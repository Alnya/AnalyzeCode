
public class Student {
	private String name;
	private String id;
	
	public Student() {
		
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return this.name;
	}
	public String getId() {
		return this.id;
	}
}
